using namespace std;

long long int N = 0;
long long int p = 0;
double b = 0;

int main () {
	cin >> N;
	long long int ns[N];
	double notes[N];
	
	for (long long int i = 0;i < N; i++){
		cin >> ns[i] >> notes[i];
		p = (notes[i] > b)? i : p;
		b = max(notes[i],b);
	}
	
	if (b < 8) cout << "Minimum note not reached\n";
	else cout << ns[p] << '\n';
	
	return 0;
}
