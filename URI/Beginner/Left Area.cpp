#include <bits/stdc++.h>

using namespace std;

char opcode;
double t = 0, r = 0;
int N = 12, l = 1, i = 0, v = 1;

int main () {
	cin >> opcode;

	while (i < 144){
		cin >> t;
		i++;
		while ((i >= N) && (i < (l*12)+v) && i < 144) {
			cin >> t;
			r += t;
			i++;
		}
		if (i == (l*12)+v) { N+=12; v += (l > 5)? -1: (v==5)? 0 : 1;l++;}
	}

	if (opcode == 'S') printf("%.1f\n", r);
	else printf("%.1f\n", r/30);

	return 0;
}
