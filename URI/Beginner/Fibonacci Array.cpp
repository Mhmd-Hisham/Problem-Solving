unsigned long long int one, two, n;

int main () {
	scanf("%d", &T);

	while ( T-- ){
		one = 0; two = 1;
		scanf("%d", &N);

		n = (N==2)? 1 : N;
		for (int i = 2; i <= N; i++){
			n = one + two;
			one = two;
			two = n;
		}

		printf("Fib(%d) = %lld\n", N, n);
	}
	
	return 0;
}

